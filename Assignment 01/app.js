"use strict";
// Example of string data type with concatenation
// Declaring variables of string data type
let firstName = "John";
let lastName = "Doe";
// Concatenating strings using the + operator
let fullName = firstName + " " + lastName;
// Output the concatenated string
console.log("Full Name: " + fullName);
// Example of number data type with arithmetic operations
// Declaring variables of number data type
let num1 = 10;
let num2 = 5;
// Performing arithmetic operations
let addition = num1 + num2;
let subtraction = num1 - num2;
let multiplication = num1 * num2;
let division = num1 / num2;
// Output the results of arithmetic operations
console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
// Example of boolean data type
// Declaring variables of boolean data type
let isRaining = true;
let isSunny = false;
// Output the boolean values
console.log("Is it raining?", isRaining); // Output: true
console.log("Is it sunny?", isSunny); // Output: false
// Example of null data type
// Declaring a variable of null data type
let nullValue = null;
// Output the null value
console.log("Null value:", nullValue); // Output: null
// Example of undefined data type
// Declaring a variable of undefined data type
let undefinedValue = undefined;
// Output the undefined value
console.log("Undefined value:", undefinedValue); // Output: undefined
// Example of symbol data type
// Declaring a symbol
const mySymbol = Symbol("mySymbol");
// Output the symbol
console.log("Symbol:", mySymbol); // Output: Symbol(mySymbol)
// Example of bigint data type
// Declaring a bigint
const myBigint = BigInt(9007199254740991);
// Output the bigint
console.log("Bigint:", myBigint); // Output: Bigint: 9007199254740991n
